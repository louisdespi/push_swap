#include "libft.h"

int		ft_atoi(char const *s)
{
	int		nb;
	int		sign;

	sign = 1;
	nb = 0;
	while (*s == '\t' || *s == '\n' || *s == '\r' || *s == '\v' || *s == '\f'
			|| *s == ' ')
		s++;
	if (*s == '-')
	{
		sign = -sign;
		s++;
	}
	else if (*s == '+')
		s++;
	while (ft_isdigit((int)*s))
	{
		nb = nb * 10 + (int)*s - 48;
		s++;
	}
	return (nb * sign);
}
