#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*final;
	size_t	i;

	final = s1;
	while (*s1)
		s1++;
	i = 0;
	while (i < n && *s2)
	{
		*s1++ = *s2++;
		i++;
	}
	*s1 = '\0';
	return (final);
}
